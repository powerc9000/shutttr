<h1>Guidelines</h1>

<p>Shutttr is a community of people with a passion for photography. In order to keep
the community healthy, we ask that you follow this set of guidelines:</p>

<ul>
<li>Be respectful of others when commenting or critiquing. This isn't to say that you
shouldn't post negative review, however, we ask that you refrain from posting
negative feedback without giving an explanation to back it, and that you avoid
being intentionally offensive or rude.</li>
<li>Maintain a high standard for the material you post. We don't expect all photos to
be professional-grade, but we expect to see work that shows effort. Shutttr is a
tool to help you improve as a photographer by receiving useful feedback. Please
post only photos that demonstrate your full ability so that critiques can provide
you with relevant information.
<ul>
<li>While some cell phone photography is very artistic, most is casual. Please use
digression when posting phone photography.</li>
<li><strong>Please</strong> do not post Instagram-style shots. This includes most photos with
extreme-filters, crazy crops or very unusual editing. There certainly are some
very valuable examples of this kind of photography, but be especially critical
when posting such shots</li>
</ul></li>
<li>Shutttr is a place for <em>photography</em> not other digital media. Please do not post
screenshots, CG art, graphic designs, etc. Keep the community on-topic.</li>
<li>Write descriptions, comments, critiques and questions in an intelligent and
meaningful way. This will encourage others to help you if you're asking a question,
to give you more relevant feedback if you're posting a photo and to be more
appreciative if you're giving feedback or answering a question.</li>
<li>Illegal material, material that promotes violence or drug use, or intentionally
offensive material (in racial, sexual, cultural or political ways) is not accepted
on Shutttr, regardless of how artistic it is. We do not maintain a no-tolerance
policy for material containing nudity, however, <em>please</em> keep it tasteful and
artistic. Posting tasteless, derogatory, illegal, or otherwise offensive nudity
may result in suspension of your account.</li>
<li>In order to keep Shutttr organized, please choose the appropriate format for giving
feedback. Comments are meant to be short responses. "That's a cool lighting effect"
or "<em>Really</em> cool bokeh you got there". Keeping comments tweet-length is a good
measure. Critiques are for giving more in-depth feedback. A good way to distinguish
the two is that critiques should give suggestions for how to improve the photo if
the poster were to take it again. Comments are your immediate reaction, pointing
out an especially good or poor part of the photo, but not analyzing it.</li>
</ul>
